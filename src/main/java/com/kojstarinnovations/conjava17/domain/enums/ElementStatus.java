package com.kojstarinnovations.conjava17.domain.enums;

/**
 * Enum to handle the status of the elements
 *
 * NEW: The element is new and has not been persisted yet
 * UPDATED: The element has been updated and has not been persisted yet
 * PERSISTED: The element has been persisted
 * DELETED: The element has been deleted
 *
 * @author Augusto Vicente
 */
public enum ElementStatus {
    NEW,
    UPDATED,
    DELETED
}
